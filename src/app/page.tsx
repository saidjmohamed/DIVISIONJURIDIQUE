// Enhanced Normalize function for Arabic text search
function normalize(text: string): string {
  if (!text) return '';
  
  return text
    // تحويل للأحرف الصغيرة
    .toLowerCase()
    // إزالة التشكيل (حركات)
    .replace(/[\u064B-\u065F]/g, '')
    // توحيد الهمزات (أ إ آ → ا)
    .replace(/[أإآ]/g, 'ا')
    // توحيد الياء (ى → ي)
    .replace(/ى/g, 'ي')
    // توحيد الهاء والتاء المربوطة (ة → ه)
    .replace(/ة/g, 'ه')
    // إزالة "ال" التعريف من بداية الكلمة
    .replace(/\bال/g, '')
    // إزالة المسافات الزائدة
    .trim();
}

// Fuzzy search score - بحث ضبابي بالأحرف المتتالية
function getFuzzyScore(query: string, text: string): number {
  let qi = 0;
  let matches = 0;
  
  for (let i = 0; i < text.length && qi < query.length; i++) {
    if (text[i] === query[qi]) {
      matches++;
      qi++;
    }
  }
  
  return qi === query.length ? matches / text.length : 0;
}

// Calculate match score for ranking results
function getMatchScore(query: string, municipalityName: string): number {
  const q = normalize(query);
  const m = normalize(municipalityName);
  
  // تطابق تام → أعلى درجة
  if (m === q) return 100;
  
  // يبدأ بنفس الكلمة → درجة عالية جداً
  if (m.startsWith(q)) return 90;
  
  // يحتوي على الكلمة كاملة → درجة عالية
  if (m.includes(q)) return 80;
  
  // كل كلمات البحث موجودة في الاسم
  const queryWords = q.split(' ').filter(w => w.length > 1);
  const allWordsMatch = queryWords.every(word => m.includes(word));
  if (allWordsMatch && queryWords.length > 0) return 70;
  
  // بعض كلمات البحث موجودة
  const someWordsMatch = queryWords.some(word => word.length > 2 && m.includes(word));
  if (someWordsMatch) return 50;
  
  // بحث بالأحرف المتفرقة (Fuzzy)
  const fuzzyScore = getFuzzyScore(q, m);
  if (fuzzyScore > 0.5) return Math.floor(fuzzyScore * 40);
  
  return 0; // لا تطابق
}

// Highlight matching text in results - simplified version
function highlightMatch(text: string, query: string): React.ReactNode {
  if (!query) return text;
  
  const q = normalize(query);
  const t = normalize(text);
  
  // البحث عن تطابق في النص المُطبع
  const matchIndex = t.indexOf(q);
  
  if (matchIndex === -1) {
    // إذا لم نجد تطابقاً مباشراً، نبحث عن كلمات منفصلة
    const words = q.split(' ').filter(w => w.length > 1);
    for (const word of words) {
      const wordIndex = t.indexOf(word);
      if (wordIndex !== -1) {
        // تمييز الكلمة في النص الأصلي
        return highlightWord(text, word);
      }
    }
    return text;
  }
  
  // البحث عن موقع البداية في النص الأصلي
  let originalStart = 0;
  let normalizedCount = 0;
  
  for (let i = 0; i < text.length; i++) {
    const normalizedChar = normalize(text[i]);
    if (normalizedChar) {
      if (normalizedCount === matchIndex) {
        originalStart = i;
        break;
      }
      normalizedCount++;
    }
  }
  
  // البحث عن موقع النهاية في النص الأصلي
  let originalEnd = originalStart;
  let matchedChars = 0;
  
  for (let i = originalStart; i < text.length && matchedChars < q.length; i++) {
    const normalizedChar = normalize(text[i]);
    if (normalizedChar) {
      matchedChars++;
    }
    originalEnd = i + 1;
  }
  
  // بناء النتيجة مع التمييز
  const before = text.substring(0, originalStart);
  const match = text.substring(originalStart, originalEnd);
  const after = text.substring(originalEnd);
  
  return (
    <>
      {before}
      <strong className="text-[#1a3a5c] font-bold bg-yellow-100 px-0.5 rounded">{match}</strong>
      {after}
    </>
  );
}

// Helper function to highlight a word in text
function highlightWord(text: string, word: string): React.ReactNode {
  const normalizedText = normalize(text);
  const wordIndex = normalizedText.indexOf(word);
  
  if (wordIndex === -1) return text;
  
  // Find original position
  let originalStart = 0;
  let normalizedCount = 0;
  
  for (let i = 0; i < text.length; i++) {
    const nc = normalize(text[i]);
    if (nc) {
      if (normalizedCount === wordIndex) {
        originalStart = i;
        break;
      }
      normalizedCount++;
    }
  }
  
  // Find end position
  let originalEnd = originalStart;
  let matchedChars = 0;
  
  for (let i = originalStart; i < text.length && matchedChars < word.length; i++) {
    const nc = normalize(text[i]);
    if (nc) {
      matchedChars++;
    }
    originalEnd = i + 1;
  }
  
  const before = text.substring(0, originalStart);
  const match = text.substring(originalStart, originalEnd);
  const after = text.substring(originalEnd);
  
  return (
    <>
      {before}
      <strong className="text-[#1a3a5c] font-bold bg-yellow-100 px-0.5 rounded">{match}</strong>
      {after}
    </>
  );
}

