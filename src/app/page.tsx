// Toast Notification System
const Toast = ({ message, type, visible, onHide }: {
  message: string;
  type: 'success' | 'info' | 'warning' | 'error';
  visible: boolean;
  onHide: () => void;
}) => {
  useEffect(() => {
    if (visible) {
      const timer = setTimeout(onHide, 2500);
      return () => clearTimeout(timer);
    }
  }, [visible, onHide]);

  const colors = {
    success: '#22c55e',
    info: '#3b82f6',
    warning: '#f59e0b',
    error: '#ef4444',
  };

  const icons = {
    success: '✅',
    info: 'ℹ️',
    warning: '⚠️',
    error: '❌',
  };

  if (!visible) return null;

  return (
    <div
      className="fixed z-[9999] flex items-center gap-2 px-5 py-3 rounded-[28px] text-white font-semibold text-sm"
      style={{
        bottom: 'calc(90px + env(safe-area-inset-bottom))',
        left: '50%',
        transform: 'translateX(-50%)',
        background: colors[type],
        boxShadow: '0 12px 40px rgba(0,0,0,0.14), 0 4px 12px rgba(0,0,0,0.08)',
        whiteSpace: 'nowrap',
        direction: 'rtl'
      }}
    >
      <span>{icons[type]}</span>
      <span>{message}</span>
    </div>
  );
};

