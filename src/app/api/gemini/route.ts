export const maxDuration = 30;

export async function POST(req: NextRequest) {
  // Rate limiting: 15 requests per minute per IP
  const rl = await checkRateLimit(req, { key: 'gemini-chat', limit: 15, window: 60 });
  if (rl.limited) {
    return NextResponse.json({ error: rl.errorMessage }, { status: 429 });
  }

  let userMessage: string, messages: Array<{ role: string; content: string }>;
  try {
    const body = await req.json();
    userMessage = body.userMessage?.trim() || "";
    messages = body.messages || [];
  } catch {
    return NextResponse.json({ error: "طلب غير صالح" }, { status: 400 });
  }

  if (!userMessage) {
    return NextResponse.json({ error: "الرسالة فارغة" }, { status: 400 });
  }

  if (userMessage.length > 30_000) {
    return NextResponse.json({ error: "الرسالة طويلة جداً. الحد الأقصى 30,000 حرف." }, { status: 400 });
  }

  try {
    const result = await callAI({
      systemPrompt: SYSTEM_PROMPT,
      userMessage,
      messages: messages.slice(-10).filter(m => m.role === 'user' || m.role === 'assistant'),
      requestType: 'chat',
      temperature: 0.7,
    });

    if (result.content) {
      return NextResponse.json({ reply: result.content });
    }

    return NextResponse.json(
      { error: result.timedOut ? "تجاوز وقت الانتظار. يرجى المحاولة مرة أخرى." : "لم يرجع المساعد أي نص. يرجى المحاولة مرة أخرى." },
      { status: 500 }
    );
  } catch (err) {
    const isTimeout = err instanceof Error && err.name === 'AbortError';
    console.error("AI Error:", err instanceof Error ? err.message : "Unknown error");
    return NextResponse.json(
      { error: isTimeout ? "تجاوز وقت الانتظار. يرجى المحاولة مرة أخرى." : "حدث خطأ في الخادم. يرجى المحاولة مرة أخرى." },
      { status: 500 }
    );
  }
}
