const ARTICLES_MAP: Record<DocumentType, { articles: string; typeName: string; law: string }> = {
  opening: { articles: OPENING_PETITION_ARTICLES, typeName: "عريضة افتتاحية (المستوى الابتدائي)", law: "قانون الإجراءات المدنية والإدارية 08-09" },
  appeal: { articles: APPEAL_PETITION_ARTICLES, typeName: "عريضة استئنافية", law: "قانون الإجراءات المدنية والإدارية 08-09" },
  complaint_regular: { articles: COMPLAINT_REGULAR_ARTICLES, typeName: "شكوى عادية للنيابة", law: "قانون الإجراءات الجزائية" },
  complaint_civil: { articles: COMPLAINT_CIVIL_PARTY_ARTICLES, typeName: "شكوى مع ادعاء مدني", law: "قانون الإجراءات الجزائية" },
  complaint_direct: { articles: COMPLAINT_DIRECT_ARTICLES, typeName: "تكليف مباشر", law: "قانون الإجراءات الجزائية" },
};

function buildSystemPrompt(docType: DocumentType): string {
  const { articles, typeName, law } = ARTICLES_MAP[docType];
  const docLabel = docType.startsWith("complaint") ? "الشكوى" : "العريضة";

  return `أنت خبير قانوني جزائري متخصص في التحقق الشكلي وفقاً لـ${law}.

مهمتك: تحليل نص ${docLabel} المقدمة وفحصها شكلياً للتحقق من استيفائها لجميع الشروط الشكلية المنصوص عليها قانوناً.

نوع المستند المطلوب فحصه: ${typeName}

${articles}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━
تعليمات التحليل:
━━━━━━━━━━━━━━━━━━━━━━━━━━━━

1. اقرأ نص العريضة بعناية شديدة
2. تحقق من كل شرط من الشروط المذكورة أعلاه
3. لكل شرط، حدد:
   - هل هو موجود ومستوفى (pass)
   - هل هو مفقود (fail)
   - هل يحتاج مراجعة أو غير واضح (warning)
   - هل لا يمكن التحقق منه من النص فقط (not_found)
4. الشروط المُعلَّمة كـ "critical" يجب أن تكون critical: true في النتيجة
5. احسب النتيجة الإجمالية:
   - "accepted": كل الشروط الجوهرية (critical) مستوفاة ولا توجد إخلالات كبيرة
   - "rejected": شرط جوهري واحد أو أكثر مفقود
   - "needs_review": هناك شروط غير واضحة تحتاج مراجعة بشرية
6. احسب النقاط من 0 إلى 100 بناءً على نسبة الشروط المستوفاة (الشروط الجوهرية لها وزن مضاعف)

━━━━━━━━━━━━━━━━━━━━━━━━━━━━
تنسيق الإجابة — JSON فقط:
━━━━━━━━━━━━━━━━━━━━━━━━━━━━

أجب فقط بكائن JSON صالح بالضبط بهذا الهيكل (بدون أي نص إضافي أو شرح أو markdown):

{
  "result": "accepted" | "rejected" | "needs_review",
  "score": <رقم من 0 إلى 100>,
  "checks": [
    {
      "id": "<معرّف فريد مثل court_designation>",
      "label": "<وصف الشرط بالعربية>",
      "article": "<رقم المادة مثل: م.15 ق.إ.م.إ>",
      "status": "pass" | "fail" | "warning" | "not_found",
      "critical": <true أو false>,
      "details": "<تفاصيل ما وجدته أو لم تجده في النص>"
    }
  ],
  "summary": "<ملخص عام للنتيجة بالعربية — جملتان أو ثلاث>",
  "recommendations": ["<توصية 1>", "<توصية 2>", "..."]
}

قواعد مهمة:
- أجب فقط بـ JSON صالح. لا تضع أي نص قبل أو بعد الكائن.
- لا تستخدم \`\`\`json أو أي تنسيق markdown.
- كل النصوص يجب أن تكون بالعربية.
- كن دقيقاً ومحايداً في التحليل.
- إذا كان النص غير واضح أو مبتور، استخدم "not_found" للشروط التي لا يمكن التحقق منها.
- لا تختلق بيانات غير موجودة في النص.`;
}

/* ─────────────────────── معالج الطلب ─────────────────────── */

export async function POST(req: NextRequest) {
  try {
    const body = await req.json();
    const { text, pdfBase64, petitionType } = body as {
      text?: string;
      pdfBase64?: string;
      petitionType: string;
    };

    const validTypes: DocumentType[] = ["opening", "appeal", "complaint_regular", "complaint_civil", "complaint_direct"];
    if (!petitionType || !validTypes.includes(petitionType as DocumentType)) {
      return NextResponse.json({ error: "نوع المستند غير صالح" }, { status: 400 });
    }

    if (!text && !pdfBase64) {
      return NextResponse.json({ error: "لم يتم تقديم نص المستند أو ملف PDF" }, { status: 400 });
    }

    const docType = petitionType as DocumentType;
    const systemPrompt = buildSystemPrompt(docType);
    const docLabel = docType.startsWith("complaint") ? "الشكوى" : "العريضة";

    const userMessage = pdfBase64
      ? `هذه هي ${docLabel} المطلوب فحصها شكلياً. قم بتحليلها وفق التعليمات المذكورة وأرجع النتيجة بصيغة JSON فقط.`
      : `هذا هو نص ${docLabel} المطلوب فحصها شكلياً:\n\n---\n${text}\n---\n\nقم بتحليل هذا النص وفق التعليمات المذكورة وأرجع النتيجة بصيغة JSON فقط.`;

    const result = await callAI({
      systemPrompt,
      userMessage,
      pdfBase64: pdfBase64 || undefined,
      task: "analysis",
    });

    let parsed;
    try {
      parsed = parseAIJson(result.text);
    } catch {
      console.error("JSON parse failed:", result.text.slice(0, 500));
      return NextResponse.json(
        { error: "تعذّر قراءة نتيجة التحليل. يرجى المحاولة مرة أخرى." },
        { status: 500 }
      );
    }

    // Validate basic structure
    const p = parsed as Record<string, unknown>;
    if (!p.result || !Array.isArray(p.checks)) {
      return NextResponse.json(
        { error: "نتيجة التحليل غير مكتملة. يرجى المحاولة مرة أخرى." },
        { status: 500 }
      );
    }

    return NextResponse.json({ analysis: parsed, provider: result.provider });
  } catch (err) {
    console.error("Error:", err);
    return NextResponse.json(
      { error: err instanceof Error ? err.message : String(err) },
      { status: 500 }
    );
  }
}
