// تطبيع النص العربي — لمعالجة اختلافات كتابة الحروف العربية
// ═══════════════════════════════════════════════════════════════
function normalizeArabic(text: string): string {
  return text
    // إزالة التشكيل
    .replace(/[\u0610-\u061A\u064B-\u065F\u0670\u06D6-\u06DC\u06DF-\u06E4\u06E7\u06E8\u06EA-\u06ED]/g, '')
    // توحيد الهمزة
    .replace(/أ/g, 'ا')
    .replace(/إ/g, 'ا')
    .replace(/آ/g, 'ا')
    .replace(/ٱ/g, 'ا')
    .replace(/ؤ/g, 'و')
    .replace(/ئ/g, 'ي')
    // توحيد التاء المربوطة والهاء
    .replace(/ة/g, 'ه')
    // توحيد الياء
    .replace(/ى/g, 'ي')
    // إزالة المسافات الزائدة
    .replace(/\s+/g, ' ')
    .trim();
}

function highlightMatch(text: string, query: string): React.ReactNode {
  if (!query || query.length < 2) return text;
  const normalizedText = normalizeArabic(text);
  const normalizedQuery = normalizeArabic(query);
  const queryIndex = normalizedText.indexOf(normalizedQuery);
  
  if (queryIndex === -1) return text;
  
  // Map back to original text positions
  let normIdx = 0;
  let startOrig = -1;
  let endOrig = -1;
  
  for (let i = 0; i < text.length; i++) {
    const normChar = normalizeArabic(text[i]);
    if (normChar.length > 0) {
      if (normIdx === queryIndex && startOrig === -1) startOrig = i;
      if (normIdx === queryIndex + normalizedQuery.length - 1) endOrig = i + 1;
      normIdx += normChar.length;
    }
  }
  
  if (startOrig === -1 || endOrig === -1) return text;
  
  return (
    <>
      {text.substring(0, startOrig)}
      <span className="text-green-600 dark:text-green-400 font-black bg-green-50 dark:bg-green-900/30 px-0.5 rounded">
        {text.substring(startOrig, endOrig)}
      </span>
      {text.substring(endOrig)}
    </>
  );
}

