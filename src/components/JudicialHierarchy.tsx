function normalize(text: string): string {
  if (!text) return '';
  return text
    .toLowerCase()
    .replace(/[\u064B-\u065F]/g, '')
    .replace(/[أإآ]/g, 'ا')
    .replace(/ى/g, 'ي')
    .replace(/ة/g, 'ه')
    .replace(/\bال/g, '')
    .trim();
}

export default function JudicialHierarchy() {
  const [query, setQuery] = useState('');
  const [results, setResults] = useState<any[]>([]);

  const handleSearch = (q: string) => {
    setQuery(q);
    if (!q.trim()) {
      setResults([]);
      return;
    }

    const normalizedQuery = normalize(q);
    const found: any[] = [];

    // Search in Ordinary Courts
    ordinaryCourtsData.forEach(entry => {
      entry.municipalities.forEach(m => {
        if (normalize(m).includes(normalizedQuery)) {
          found.push({
            municipality: m,
            type: 'ordinary',
            court: entry.court,
            council: entry.council
          });
        }
      });
    });

    // Search in Admin Courts
    adminCourtsData.forEach(entry => {
      entry.municipalities.forEach(m => {
        if (normalize(m).includes(normalizedQuery)) {
          found.push({
            municipality: m,
            type: 'admin',
            court: entry.adminCourt,
            appeal: entry.appealCourt
          });
        }
      });
    });

    setResults(found);
  };

  return (
    <div className="space-y-6" dir="rtl">
      <div className="relative">
        <input
          type="text"
          value={query}
          onChange={(e) => handleSearch(e.target.value)}
          placeholder="ابحث عن بلدية لمعرفة اختصاصها الإقليمي..."
          className="w-full p-4 pr-12 rounded-2xl border border-gray-200 dark:border-gray-700 bg-white dark:bg-gray-800 shadow-sm focus:ring-2 focus:ring-[#1a3a5c] outline-none transition-all"
        />
        <span className="absolute right-4 top-1/2 -translate-y-1/2 text-xl">🔍</span>
      </div>

      {results.length > 0 ? (
        <div className="grid gap-4">
          {results.map((res, idx) => (
            <div key={idx} className="bg-white dark:bg-gray-800 p-5 rounded-2xl border border-gray-100 dark:border-gray-700 shadow-sm hover:shadow-md transition-all">
              <div className="flex items-center justify-between mb-4">
                <span className="text-lg font-bold text-[#1a3a5c] dark:text-[#f0c040]">📍 بلدية {res.municipality}</span>
                <span className={`text-[10px] px-3 py-1 rounded-full font-bold ${res.type === 'admin' ? 'bg-purple-100 text-purple-700' : 'bg-blue-100 text-blue-700'}`}>
                  {res.type === 'admin' ? 'قضاء إداري' : 'قضاء عادي'}
                </span>
              </div>
              
              <div className="grid grid-cols-2 gap-4">
                <div className="space-y-1">
                  <p className="text-[10px] text-gray-500 uppercase tracking-wider">المحكمة المختصة</p>
                  <p className="text-sm font-bold text-gray-800 dark:text-white">{res.court}</p>
                </div>
                <div className="space-y-1">
                  <p className="text-[10px] text-gray-500 uppercase tracking-wider">{res.type === 'admin' ? 'محكمة الاستئناف' : 'مجلس القضاء'}</p>
                  <p className="text-sm font-bold text-gray-800 dark:text-white">{res.type === 'admin' ? res.appeal : res.council}</p>
                </div>
              </div>
            </div>
          ))}
        </div>
      ) : query.trim() ? (
        <div className="text-center py-12 bg-gray-50 dark:bg-gray-900/50 rounded-3xl border-2 border-dashed border-gray-200 dark:border-gray-800">
          <p className="text-gray-500 dark:text-gray-400">لا توجد نتائج لهذه البلدية حالياً.</p>
        </div>
      ) : (
        <div className="bg-blue-50 dark:bg-blue-900/10 p-6 rounded-3xl border border-blue-100 dark:border-blue-800">
          <h4 className="font-bold text-[#1a3a5c] dark:text-blue-400 mb-2">💡 نصيحة</h4>
          <p className="text-sm text-blue-700 dark:text-blue-300 leading-relaxed">
            اكتب اسم البلدية (مثلاً: البليدة، سيدي امحمد، وهران) لمعرفة المحكمة ومجلس القضاء المختص إقليمياً.
          </p>
        </div>
      )}
    </div>
  );
}
