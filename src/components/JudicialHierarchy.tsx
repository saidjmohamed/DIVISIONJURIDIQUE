  const [searchQuery, setSearchQuery] = useState('');
  const [selectedResult, setSelectedResult] = useState<SearchResult | null>(null);
  const [isDropdownOpen, setIsDropdownOpen] = useState(false);
  const [activeIndex, setActiveIndex] = useState(-1);
  const dropdownRef = useRef<HTMLDivElement>(null);
  const inputRef = useRef<HTMLInputElement>(null);

  // ═══════════════════════════════════════════════════════════
  // محرك البحث الذكي مع التطبيع العربي
  // ═══════════════════════════════════════════════════════════
  const suggestions = useMemo(() => {
    const trimmed = searchQuery.trim();
    if (trimmed.length < 1) return [];

    const normalizedQuery = normalizeArabic(trimmed);
    const results: SearchResult[] = [];
    const seen = new Set<string>();

    judicialData.forEach(entry => {
      entry.municipalities.forEach(muni => {
        const normalizedMuni = normalizeArabic(muni);
        const normalizedCourt = normalizeArabic(entry.court);
        const normalizedCouncil = normalizeArabic(entry.council);
        const normalizedWilaya = normalizeArabic(entry.wilaya || entry.council);

        let score = 0;

        // مطابقة كاملة للبلدية
        if (normalizedMuni === normalizedQuery) {
          score = 100;
        }
        // البلدية تبدأ بالاستعلام
        else if (normalizedMuni.startsWith(normalizedQuery)) {
          score = 85;
        }
        // الاستعلام موجود داخل البلدية
        else if (normalizedMuni.includes(normalizedQuery)) {
          score = 70;
        }
        // مطابقة المحكمة
        else if (normalizedCourt.includes(normalizedQuery)) {
          score = 50;
        }
        // مطابقة مجلس القضاء
        else if (normalizedCouncil.includes(normalizedQuery)) {
          score = 40;
        }
        // مطابقة الولاية
        else if (normalizedWilaya.includes(normalizedQuery)) {
          score = 35;
        }
        // مطابقة الأحرف بترتيب متسلسل (subsequence matching)
        else {
          let qi = 0;
          for (let mi = 0; mi < normalizedMuni.length && qi < normalizedQuery.length; mi++) {
            if (normalizedMuni[mi] === normalizedQuery[qi]) qi++;
          }
          if (qi === normalizedQuery.length) {
            score = 25;
          }
        }

        if (score > 0) {
          const key = `${muni}-${entry.court}`;
          if (!seen.has(key)) {
            seen.add(key);
            const admin = adminJudicialData[entry.council] || {
              court: `المحكمة الإدارية بـ ${entry.council}`,
              appellate: "المحكمة الإدارية الاستئنافية المختصة"
            };
            results.push({
              municipality: muni,
              court: entry.court,
              council: `مجلس قضاء ${entry.council}`,
              wilaya: entry.wilaya || entry.council,
              adminCourt: admin.court,
              adminAppellate: admin.appellate,
              entry: entry,
              score: score
            });
          }
        }
      });
    });

    // ترتيب حسب النقاط ثم أبجدياً
    results.sort((a, b) => {
      if (b.score !== a.score) return b.score - a.score;
      return a.municipality.localeCompare(b.municipality, 'ar');
    });

    return results.slice(0, 50); // عرض حتى 50 نتيجة
  }, [searchQuery]);

