export default function PetitionTemplates({ onBack }: { onBack: () => void }) {
  const [selectedTemplate, setSelectedTemplate] = useState<string | null>(null);
  const [copyFeedback, setCopyFeedback] = useState(false);

  const template = templates.find(t => t.id === selectedTemplate);

  const copyTemplate = () => {
    if (template) {
      navigator.clipboard.writeText(template.content).then(() => {
        setCopyFeedback(true);
        setTimeout(() => setCopyFeedback(false), 2000);
      }).catch(() => {});
    }
  };

  if (template) {
    return (
      <div className="max-w-2xl mx-auto" dir="rtl">
        <div className="flex items-center gap-3 mb-4">
          <button onClick={() => setSelectedTemplate(null)} className="text-[#1a3a5c] dark:text-[#f0c040] text-lg">→</button>
          <h2 className="text-lg font-bold text-[#1a3a5c] dark:text-[#f0c040]">{template.icon} {template.title}</h2>
        </div>
        <div className="bg-white dark:bg-gray-800 rounded-xl p-4 mb-4 border border-gray-200 dark:border-gray-700">
          <pre className="whitespace-pre-wrap text-sm text-gray-800 dark:text-gray-200 leading-relaxed font-sans" dir="rtl">
            {template.content}
          </pre>
        </div>
        <button onClick={copyTemplate}
          className={`w-full py-2.5 rounded-lg text-sm font-medium transition-all ${copyFeedback ? 'bg-green-500 text-white' : 'bg-[#d97706] text-white'}`}>
          {copyFeedback ? '✓ تم النسخ' : '📋 نسخ النموذج'}
        </button>
      </div>
    );
  }

  return (
    <div className="max-w-2xl mx-auto" dir="rtl">
      <div className="flex items-center gap-3 mb-4">
        <button onClick={onBack} className="text-[#1a3a5c] dark:text-[#f0c040] text-lg">→</button>
        <h2 className="text-lg font-bold text-[#1a3a5c] dark:text-[#f0c040]">📄 نماذج العرائض</h2>
      </div>
      <p className="text-sm text-gray-500 dark:text-gray-400 mb-4">نماذج جاهزة يمكنك نسخها وتعديلها حسب الحاجة</p>
      <div className="space-y-3">
        {templates.map(t => (
          <button key={t.id} onClick={() => setSelectedTemplate(t.id)}
            className="w-full flex items-center gap-3 p-4 bg-white dark:bg-gray-800 rounded-xl border border-gray-200 dark:border-gray-700 hover:shadow-md transition-all text-right">
            <span className="text-2xl">{t.icon}</span>
            <span className="flex-1 text-sm font-medium text-gray-800 dark:text-gray-200">{t.title}</span>
            <span className="text-gray-400 text-sm">←</span>
          </button>
        ))}
      </div>
    </div>
  );
}
